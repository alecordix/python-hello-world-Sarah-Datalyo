# On affecte nos variables
unString = "string"  # une chaîne de caractères
unInteger = 2  # un entier
unFloat = 3.0  # un décimal
unBoolean = True  # un booléen

# On affiche nos variables
print(unString)
print(unInteger)
print(unFloat)
print(unBoolean)


# On peut connaître le type de nos variables à l'aide
# de la fonction type(maVariable)
print(type(unString))
print(type(unInteger))
print(type(unFloat))
print(type(unBoolean))
